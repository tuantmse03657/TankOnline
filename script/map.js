function drawmap(context)
{
    for(var i = 0;i<arrBrick.length;i++)
    {
        arrBrick[i].draw(context);
    }
    for(var i = 0;i<arrSteel.length;i++)
    {
        arrSteel[i].draw(context);
    }
    for(var i = 0;i<arrWater.length;i++)
    {
        arrWater[i].draw(context);
    }
    for(var i = 0;i<arrTree.length;i++)
    {
        arrTree[i].draw(context);
    }
    
}
function savebrick(){
    arrBrick = [];
    arrSteel = [];
    arrWater = [];
    arrTree = [];
    var posx=16;
    var posy=16;
    for(var i = 0; i<26;i++)
    {
        for(var j =0;j<26;j++)
        {
            if(map[i][j]==1)
            {
                var wall = new Wall(posx,posy);
                arrBrick.push(wall);
            }
            if(map[i][j]==2)
            {
                var wall2 = new Wall2(posx,posy);
                arrSteel.push(wall2);
            }
            if(map[i][j]==3 && map[i][j+1]==3 && map[i+1][j]==3 && map[i+1][j+1]==3)
            {
                var water = new Water(posx,posy);
                arrWater.push(water);
            }
            if(map[i][j]==4 && map[i][j+1]==4 && map[i+1][j]==4 && map[i+1][j+1]==4)
            {
                var tree = new Tree(posx,posy);
                arrTree.push(tree);
            }
            posx+=16;
        }
        posx=0;
        posy +=16;
    }
}

